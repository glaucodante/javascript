// ENQUANTO ESSA CONDIÇÃO FOR SATISFATORIA VAI RODAR


let html = '';

let c = 0;


while (c < 10) {

    html += "Número: " + c + "<br/>";
    c++; //COLOCAR ESTA PROPRIEDADE PARA NAO TRAVAR O NAVEGADOR INDEFINIDAMENTE 

}


// C++ = C + C = 1 // C+=1                  // C++ = AUMENTA MAIS UM

//SE FOR USAR O FOR

for (let c = 1; c <= 10; c++) {
    html += 'Número:' + c + '<br/>';
}


// OS DOIS TEM A MESMA FUNÇÃO




document.getElementById('demo').innerHTML = html;























